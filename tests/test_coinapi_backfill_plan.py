def _plan(path, **kw):
    kw.setdefault("generated_utc", "2026-07-10T01:00:00Z")
    return bf.build_plan(str(path), **kw)


def _units_by_key(plan):
    return {(u["product"], u["day"]): u for u in plan["units"]}


