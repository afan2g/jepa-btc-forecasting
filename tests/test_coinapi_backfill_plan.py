# =========================================================================== execution report
def _result(unit, status, **kw):
    base = {"source": "coinapi_flatfiles", "product": unit["product"], "day": unit["day"],
            "kind": unit.get("kind", "full_day"), "status": status, "key": None,
            "src_bytes": 0, "src_sha256": None, "rows": 0, "out_bytes": 0, "out_sha256": None,
            "out_path": None, "secs": 0.0, "error": None}
    base.update(kw)
    return base


def test_execution_report_reconciles(tmp_path):
    path, _ = fx.ready_manifest(tmp_path)
    plan = _plan(path)
    units = plan["units"]
    results = [
        _result(units[0], "ok", src_bytes=1_000_000, src_sha256="s0", rows=10,
                out_bytes=500, out_sha256="o0", out_path="x", key="k0"),
        _result(units[1], "ok", src_bytes=2_000_000, src_sha256="s1", rows=20,
                out_bytes=600, out_sha256="o1", out_path="y", key="k1"),
        _result(units[2], "done"),
        _result(units[3], "missing"),
        _result(units[4], "error", error="boom"),
    ]
    spend = {"approve_usd": 10.0, "spend_evidence": "issue #33 approval",
             "allow_backfill": True}
    report = bf.build_execution_report(plan, results, spend=spend,
                                       generated_utc="2026-07-10T02:00:00Z")
    rec = report["reconciliation"]
    assert rec["planned"] == 5 and rec["accounted"] == 5
    assert rec["ok"] == 2 and rec["done_prior"] == 1 and rec["missing"] == 1
    assert rec["error"] == 1 and rec["conflict"] == 0
    assert rec["complete"] is False
    assert rec["bytes_downloaded"] == 3_000_000
    assert rec["rows_written"] == 30
    assert report["spend"]["approve_usd"] == 10.0
    assert report["spend"]["spend_evidence"] == "issue #33 approval"
    assert report["spend"]["planned_usd_high"] == plan["totals"]["usd_high"]
    assert report["meta"]["manifest"]["sha256"] == plan["meta"]["manifest"]["sha256"]
    assert len(report["units"]) == 5
    # every unit result keeps its provenance hash fields for the audit trail
    assert report["units"][0]["src_sha256"] == "s0" and report["units"][0]["out_sha256"] == "o0"


