"""Offline tests for the CoinAPI reviewed-manifest backfill planning core
(`ingest/coinapi_backfill.py`, issue #53). Stdlib-only module, synthetic manifests generated by
the REAL reviewer (tests/coinapi_backfill_fixtures.py) — no vendor I/O anywhere.

Covers: the fail-closed acceptance gate (ready/scope-complete/hash/stale/malformed), sparse unit
derivation (exact fill units, never intervening dates), verbatim stitch/provenance preservation,
sections + cost reconciliation against the reviewed manifest, the hash-pinned pilot-window subset,
resume-state keying on source/product/day + manifest fingerprint, and execution-report
reconciliation."""
