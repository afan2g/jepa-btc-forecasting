def test_pilot_window_subset_is_deterministic_and_hash_pinned(tmp_path):
    path, _ = fx.ready_manifest(tmp_path)
    full = _plan(path)
    sub = _plan(path, window_start="2025-01-10", window_end="2025-01-11")
    keys = [(u["product"], u["day"]) for u in sub["units"]]
    assert keys == [("limitbook_full", "2025-01-10"), ("trades", "2025-01-10"),
                    ("trades", "2025-01-11")]
    assert sub["meta"]["window"] == {"start": "2025-01-10", "end": "2025-01-11"}
    assert sub["meta"]["manifest"]["sha256"] == full["meta"]["manifest"]["sha256"]
    # fill policy NOT recomputed: subset units carry the identical provenance/cost rows
    fu, su = _units_by_key(full), _units_by_key(sub)
    for k in keys:
        assert su[k] == fu[k]
    # subset totals are sums over the selected units only
    assert sub["totals"]["n_units"] == 3
    assert sub["totals"]["book_gb_total"] == 1.0
    assert sub["totals"]["trades_gb_total"] == 0.05
    assert sub["totals"]["gross_usd"] == round(1.0 * 1.0 + 0.05 * 3.0, 4)
    # the FULL scope still reconciles against the manifest even when a window is applied
    assert sub["reconciliation"]["matches_manifest_cost_summary"] is True
    assert sub["scope"]["skipped_by_window"] == 2


